package org.n52.sos.importer.model.dateAndTime;

public class DateModel {

	private int day = -1;
	
	private int month = -1;
	
	private int year = -1;

	public int getDay() {
		return day;
	}

	public void setDay(int day) {
		this.day = day;
	}

	public int getMonth() {
		return month;
	}

	public void setMonth(int month) {
		this.month = month;
	}

	public int getYear() {
		return year;
	}

	public void setYear(int year) {
		this.year = year;
	}
}
